#ifndef __DTS_SILVERBULLET_PINCTRL_H
#define __DTS_SILVERBULLET_PINCTRL_H

#define PAD_CTL_SRE_SLOW	0x00000000	// (0 << 0)
#define PAD_CTL_SRE_FAST	0x00000001	// (1 << 0)
#define PAD_CTL_DSE_DISABLE	0x00000000	// (0 << 3)
#define PAD_CTL_DSE_240ohm	0x00000008	// (1 << 3)
#define PAD_CTL_DSE_120ohm	0x00000010	// (2 << 3)
#define PAD_CTL_DSE_80ohm	0x00000018	// (3 << 3)
#define PAD_CTL_DSE_60ohm	0x00000020	// (4 << 3)
#define PAD_CTL_DSE_48ohm	0x00000028	// (5 << 3)
#define PAD_CTL_DSE_40ohm	0x00000030	// (6 << 3)
#define PAD_CTL_DSE_34ohm	0x00000038	// (7 << 3)
#define PAD_CTL_SPEED_LOW	0x00000040	// (1 << 6)
#define PAD_CTL_SPEED_MED	0x00000080	// (2 << 6)
#define PAD_CTL_SPEED_HIGH	0x000000c0	// (3 << 6)
#define PAD_CTL_ODE		0x00000800	// (1 << 11)
#define PAD_CTL_PKE		0x00001000	// (1 << 12)
#define PAD_CTL_PUE		0x00002000	// (1 << 13)
#define PAD_CTL_PUS_100K_DOWN	0x00000000	// (0 << 14)
#define PAD_CTL_PUS_47K_UP	0x00004000	// (1 << 14)
#define PAD_CTL_PUS_100K_UP	0x00008000	// (2 << 14)
#define PAD_CTL_PUS_22K_UP	0x0000c000	// (3 << 14)
#define PAD_CTL_HYS		0x00010000	// (1 << 16)
#define PAD_CTL_SION		0x40000000	// (1 << 30)
#define PAD_CTL_NONE		0x80000000	// (1 << 31)

#define SILVERBULLET_PAD_CTRL_AUD	\
		(PAD_CTL_PKE | PAD_CTL_PUE | PAD_CTL_PUS_100K_DOWN |	\
		 PAD_CTL_DSE_40ohm | PAD_CTL_SPEED_MED | PAD_CTL_HYS)		// 0x000130b0

#define SILVERBULLET_PAD_CTL_ESAI	\
		(PAD_CTL_PKE | PAD_CTL_PUE | PAD_CTL_PUS_100K_UP |	\
		 PAD_CTL_DSE_40ohm | PAD_CTL_HYS)				// 0x0001b030

#define SILVERBULLET_PAD_CTRL_ECSPI	\
		(PAD_CTL_SRE_FAST | PAD_CTL_SPEED_MED | \
		 PAD_CTL_DSE_40ohm | PAD_CTL_HYS)				// 0x000100b1

#define SILVERBULLET_PAD_CTRL_ENET	\
		(PAD_CTL_PKE | PAD_CTL_PUE | PAD_CTL_PUS_100K_UP |	\
		 PAD_CTL_SPEED_MED | PAD_CTL_DSE_40ohm | PAD_CTL_HYS)		// 0x0001b0b0

#define SILVERBULLET_PAD_CTRL_I2C	\
		(PAD_CTL_PKE | PAD_CTL_PUE | PAD_CTL_PUS_100K_UP |	\
		 PAD_CTL_ODE | PAD_CTL_SPEED_MED | PAD_CTL_DSE_40ohm |	\
		 PAD_CTL_SRE_FAST | PAD_CTL_HYS | PAD_CTL_SION)			// 0x4001b8b1

#define SILVERBULLET_PAD_CTRL_UART	\
		(PAD_CTL_PKE | PAD_CTL_PUE | PAD_CTL_PUS_100K_UP |	\
		 PAD_CTL_SPEED_MED | PAD_CTL_DSE_40ohm |	\
		 PAD_CTL_SRE_FAST | PAD_CTL_HYS)				// 0x0001b0b1

#define SILVERBULLET_PAD_CTRL_USB_OTG_ID	\
		(PAD_CTL_PKE | PAD_CTL_PUE | PAD_CTL_PUS_47K_UP |	\
		 PAD_CTL_SPEED_LOW | PAD_CTL_DSE_80ohm |		\
		 PAD_CTL_SRE_FAST | PAD_CTL_HYS)				// 0x00017059

#define SILVERBULLET_PAD_CTRL_USB_OC	\
		(PAD_CTL_PKE | PAD_CTL_PUE | PAD_CTL_PUS_100K_UP |	\
		 PAD_CTL_SPEED_MED | PAD_CTL_DSE_40ohm | PAD_CTL_HYS)		// 0x0001b0b0

#define SILVERBULLET_PAD_CTRL_SD_SPEED_LOW	\
		(PAD_CTL_PKE | PAD_CTL_PUE | PAD_CTL_PUS_47K_UP |	\
		 PAD_CTL_SPEED_LOW | PAD_CTL_DSE_80ohm |		\
		 PAD_CTL_SRE_FAST | PAD_CTL_HYS)				// 0x00017059

#define SILVERBULLET_PAD_CTRL_SD_SPEED_MED	\
		(PAD_CTL_PKE | PAD_CTL_PUE | PAD_CTL_PUS_47K_UP |	\
		 PAD_CTL_SPEED_MED | PAD_CTL_DSE_40ohm |		\
		 PAD_CTL_SRE_FAST | PAD_CTL_HYS)				// 0x000170b1

#define SILVERBULLET_PAD_CTRL_SD_SPEED_HIGH	\
		(PAD_CTL_PKE | PAD_CTL_PUE | PAD_CTL_PUS_47K_UP |	\
		 PAD_CTL_SPEED_HIGH | PAD_CTL_DSE_34ohm |		\
		 PAD_CTL_SRE_FAST | PAD_CTL_HYS)				// 0x000170f9

#define SILVERBULLET_PAD_CTRL_CCM_CLK0	\
		(PAD_CTL_PKE | PAD_CTL_PUE | PAD_CTL_PUS_100K_DOWN |	\
		 PAD_CTL_SPEED_MED | PAD_CTL_DSE_40ohm | PAD_CTL_HYS)		// 0x000130b0

#endif /* __DTS_SILVERBULLET_PINCTRL_H */
